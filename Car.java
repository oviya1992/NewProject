package Final;

public class Car {
	String brand;
	String color;
	int price;
	public void setbrand(String brand) {
		this.brand=brand;
	}
	public String getbrand() {
		return brand;
	}
	public void setcolor(String color) {
		this.color=color;
	}
    public String getcolor() {
    	return color;
    }
    public void setprice(int price) {
    	this.price=price;
    }
    public int getprice() {
    	return price;
    }
}
